const NextButton = ({ dispatch, answer, isLastQuestion }) => {
  if (answer === null) return;

  if (!isLastQuestion)
    return (
      <button
        className="btn btn-ui"
        onClick={() => dispatch({ type: "nextQuestion" })}
      >
        Next
      </button>
    );

  if (isLastQuestion) {
    return (
      <button
        className="btn btn-ui"
        onClick={() => dispatch({ type: "finish" })}
      >
        Finish
      </button>
    );
  }
};

export default NextButton;
